package Facade;

public class PomotodoFacade {
	Start srt;
	Remove del;
	Done done;
	
	public PomotodoFacade(Start srt, Remove del, Done done) {
		super();
		this.srt = srt;
		this.del = del;
		this.done = done;
	}
	
	public void Add()
	{
		srt.getClass();
		del.getClass();
		done.getClass();
		
	}

}
